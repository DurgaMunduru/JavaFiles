//      Different types of polymorphism: 1.Compile time
//                                       2.run-time
//
//      1.Compile-time Polymorphism : is a type of polymorphism which can be achieved at java compilation time.
//                                     - also referred as static polymorphism or early binding
//                                     - can be achieved using method overloading
//
//      2.Run-time Polymorphism : is a type of  Polymorphism which can be achieved at runtime or execution time.
//                                  - referred as dynamic Polymorphism or dynamic method dispatch
//                                  -achieved by method overriding

